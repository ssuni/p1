3. 리에스 클리닉을 이용하여 법령과 본 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우


③ 리에스 클리닉이 회원의 회원자격을 상실시키기로 결정한 경우에는 회원등록을 말소합니다. 이 경우 회원인 회원에게 회원등록 말소 전에 이를 통지하고, 소명할 기회를 부여합니다.

