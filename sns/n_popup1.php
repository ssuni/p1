이 약관은 리에스 클리닉(이하 '회사')가 제공하는 서비스 이용조건 및 절차에 관한 사항과 기타 필요한 사항을 전기통신사업법 및 동법 시행령이 정하는 대로 준수하고 규정함을 목적으로 합니다

제1조(목적 등)

① 리에스 클리닉(http://www.resexy.co.kr) 이용자 약관(이하 "본 약관"이라 합니다)은 이용자가 리에스 클리닉에서 제공하는 인터넷 관련 서비스(이하 "서비스"라 합니다)를 이용함에 있어 회원과 리에스 클리닉의 권리·의무 및 책임사항을 규정함을 목적으로 합니다.

② 회원이 되고자 하는 자가 리에스 클리닉에서 정한 소정의 절차를 거쳐서 "등록하기" 단추를 누르면 본 약관에 동의하는 것으로 간주합니다. 본 약관에 정하는 이외의 회원과 리에스 클리닉의 권리, 의무 및 책임사항에 관해서는 전기통신사업법 기타 대한민국의 관련 법령과 상관습에 의합니다.

제2조(회원의 정의)

"회원"이란 리에스 클리닉에 접속하여 본 약관에 따라 리에스 클리닉 온라인 회원으로 가입하여 리에스 클리닉이 제공하는 서비스를 받는 자를 말합니다.

제3조 (회원 가입)

① 회원이 되고자 하는 자는 리에스 클리닉이 정한 가입 양식에 따라 회원정보를 기입하고 "등록하기" 단추를 누르는 방법으로 회원 가입을 신청합니다.

② 리에스 클리닉은 제1항과 같이 회원으로 가입할 것을 신청한 자가 다음 각 호에 해당하지 않는 한 신청한 자를 회원으로 등록합니다.

1. 가입신청자가 본 약관 제6조 제3항에 의하여 이전에 회원자격을 상실한 적이 있는 경우. 다만 제6조 제3항에 의한 회원자격 상실 후 3년이 경과한 자로서 리에스 클리닉의 회원 재가입 승낙을 얻은 경우에는 예외로 합니다.

2. 등록 내용에 허위, 기재누락, 오기가 있는 경우

3. 기타 회원으로 등록하는 것이 리에스 클리닉의 기술상 현저히 지장이 있다고 판단되는 경우

③ 회원가입계약의 성립시기는 리에스 클리닉의 승낙이 가입신청자에게 도달한 시점으로 합니다.

④ 회원은 제1항의 회원정보 기재 내용에 변경이 발생한 경우, 즉시 변경사항을 정정하여 기재하여야 합니다.

