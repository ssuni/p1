3. 리에스 클리닉 온라인 회원을 위한 섹션 및 컨텐츠 서비스

4. 기타 리에스 클리닉이 타 업체와 제휴해서 제공하는 각종 서비스

② 리에스 클리닉은 그 변경될 서비스의 내용 및 제공일자를 제7조 제2항에서 정한 방법으로 회원에게 통지하고, 제1항에 정한 서비스를 변경하여 제공할 수 있습니다.

제5조(서비스의 중단)

① 리에스 클리닉은 컴퓨터 등 정보통신설비의 보수점검·교체 및 고장, 통신의 두절 등의 사유가 발생한 경우에는 서비스의 제공을 일시적으로 중단할 수 있고, 새로운 서비스로의 교체 기타 리에스 클리닉이 적절하다고 판단하는 사유에 기하여 현재 제공되는 서비스를 완전히 중단할 수 있습니다.

② 제1항에 의한 서비스 중단의 경우에는 리에스 클리닉은 제7조 제2항에서 정한 방법으로 회원에게 통지합니다. 다만, 리에스 클리닉이 통제할 수 없는 사유로 인한 서비스의 중단(시스템 관리자의 고의, 과실이 없는 디스크 장애, 시스템 다운 등)으로 인하여 사전 통지가 불가능한 경우에는 그러하지 아니합니다.

제6조(회원 탈퇴 및 자격 상실 등)

① 회원은 리에스 클리닉에 언제든지 자신의 회원 등록을 말소해 줄 것(회원 탈퇴)을 요청할 수 있으며, 리에스 클리닉은 위 요청을 받은 즉시 해당 회원의 회원 등록 말소를 위한 절차를 밟습니다.

② 회원이 다음 각 호의 사유에 해당하는 경우, 리에스 클리닉은 회원의 회원자격을 적절한 방법으로 제한 및 정지, 상실시킬 수 있습니다.

